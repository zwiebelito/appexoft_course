// 4 Перевірка пароля: Створіть програму, яка перевіряє пароль користувача. Якщо пароль "секрет",
// то виведіть "Вірно!", в іншому випадку - "Невірний пароль!".

const checkPassword = (password) => {
    if (password === "секрет") {
        console.log("Вірно");
    } else {
        console.log("Невірний пароль!");
    }

}

checkPassword("секрет")
checkPassword("Опілля")